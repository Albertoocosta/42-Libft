#include "libft.h"

char	*ft_strrchr(char *str, int c)
{
	size_t	l;

	l = (ft_strlen(str)) - 1;
	while (l > 0)
	{
		if (c == str[l])
			return ((char *)str);
		str--;
	}
	return (0);
}
